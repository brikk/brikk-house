    private val json = Json { ignoreUnknownKeys = true }

    private fun loadCorpus(): Map<String, JsonArray> {
        val stream = javaClass.classLoader.getResourceAsStream("ast-corpus/handbuilt.json")
            ?: java.io.File("brikk-sql/testResources/ast-corpus/handbuilt.json")
                .takeIf { it.exists() }
                ?.inputStream()
            ?: fail("corpus ast-corpus/handbuilt.json not found on classpath or filesystem")
        val root = stream.use { json.parseToJsonElement(it.readBytes().decodeToString()) }.jsonObject
        return root.getValue("cases").jsonArray.associate { case ->
            val obj = case.jsonObject
            obj.getValue("id").jsonPrimitive.content to obj.getValue("dump").jsonArray
        }
    }

    @Test
    fun dumpsMatchPythonOracle() {
        val corpus = loadCorpus()
        check(corpus.isNotEmpty()) { "empty ast corpus" }

        val missing = corpus.keys - twins.keys
        assertTrue(missing.isEmpty(), "corpus cases without a Kotlin twin: $missing")

        val mismatches = mutableListOf<String>()
        for ((id, oracle) in corpus) {
            val ours = Serde.dump(twins.getValue(id)())
            val oursStripped = Serde.stripMetaAndComments(ours)
            val oracleStripped = Serde.stripMetaAndComments(oracle)
            if (oursStripped != oracleStripped) {
                mismatches.add("case $id\n  oracle: $oracleStripped\n  ours:   $oursStripped")
            }
        }

        if (mismatches.isNotEmpty()) {
            fail("${mismatches.size}/${corpus.size} mismatching cases:\n\n" + mismatches.joinToString("\n\n"))
        }
    }

    @Test
    fun commentsAndMetaMatchPythonOracleUnstripped() {
        // comments/meta encoding parity is checked without stripping on the case that has them
        val oracle = loadCorpus().getValue("comments_meta")
        assertEquals(oracle, Serde.dump(twins.getValue("comments_meta")()))
    }

    @Test
    fun loadDumpRoundTripsEveryTree() {
        for ((id, build) in twins) {
            val tree = build()
            val dumped = Serde.dump(tree)
            val loaded = Serde.loadExpression(dumped)
            assertEquals(tree, loaded, "round-trip equality failed for case $id")
            assertEquals(dumped, Serde.dump(loaded), "round-trip dump stability failed for case $id")
        }
    }

    @Test
    fun oracleDumpsLoadIntoEqualTrees() {
        // loading the PYTHON dump must also produce a tree equal to the hand-built twin
        for ((id, oracle) in loadCorpus()) {
            val loaded = Serde.loadExpression(oracle)
            assertEquals(twins.getValue(id)(), loaded, "oracle load mismatch for case $id")
        }
    }
