        Identifier(args("this" to name, "quoted" to quoted))

    private fun col(name: String): Column = Column(args("this" to ident(name)))

    private fun num(value: String): Literal =
        Literal(args("this" to value, "is_string" to false))

    private fun num(value: Int): Literal = num(value.toString())

    private fun lit(value: String): Literal =
        Literal(args("this" to value, "is_string" to true))

