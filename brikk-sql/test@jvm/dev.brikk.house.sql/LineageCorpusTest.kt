/**
 * Gate: our lineage() vs the Python oracle over testResources/lineage-corpus/base.json
 * (generated by tools/gen_lineage_corpus.py from reference/sqlglot tests/test_lineage.py
 * plus curated extras; unsupported-dialect cases were skipped at generation time).
 *
 * Node graphs are compared structurally: {name, expression sql, source sql, source_name,
 * reference_node_name, downstream}. Downstream children are sorted (on both sides) by a
 * Python-json.dumps-compatible canonical dump, because Python's to_node iterates a
 * set() of Column expressions whose order is hash-dependent and not portable.
 *
 * Failures must exactly match lineage-corpus/known-failures.json — no unledgered
 * failure, no stale entry. Actual failures are written to build/lineage-ledger-actual.json.
 */
class LineageCorpusTest {

    private val json = Json { ignoreUnknownKeys = true }

    private fun resource(path: String): String {
        val stream = javaClass.classLoader.getResourceAsStream(path)
            ?: java.io.File("brikk-sql/testResources/$path").takeIf { it.exists() }?.inputStream()
            ?: java.io.File("testResources/$path").takeIf { it.exists() }?.inputStream()
            ?: fail("resource $path not found on classpath or filesystem")
        return stream.use { it.readBytes().decodeToString() }
    }

    private fun loadLedger(): Map<String, String> {
        val text = try {
            resource("lineage-corpus/known-failures.json")
        } catch (e: AssertionError) {
            return emptyMap()
        }
        val root = json.parseToJsonElement(text).jsonObject
        return root.getValue("cases").jsonArray.associate { entry ->
            val obj = entry.jsonObject
            obj.getValue("key").jsonPrimitive.content to
                obj.getValue("reason").jsonPrimitive.content
        }
    }

    /** Nested JsonObject -> plain Map (schema dicts hold string leaf values). */
    private fun toPlainMap(obj: JsonObject): MutableMap<String, Any?> {
        val out = LinkedHashMap<String, Any?>()
        for ((k, v) in obj) {
            out[k] = when (v) {
                is JsonObject -> toPlainMap(v)
                is JsonArray -> v.map { (it as JsonPrimitive).content }
                is JsonPrimitive -> v.content
                else -> null
            }
        }
        return out
    }

    /** json.dumps(x, sort_keys=True) — Python-compatible canonical dump. */
    private fun pyDumps(element: JsonElement, sb: StringBuilder) {
        when (element) {
            is JsonObject -> {
                sb.append('{')
                var first = true
                for (key in element.keys.sorted()) {
                    if (!first) sb.append(", ")
                    first = false
                    pyString(key, sb)
                    sb.append(": ")
                    pyDumps(element.getValue(key), sb)
                }
                sb.append('}')
            }
            is JsonArray -> {
                sb.append('[')
                for ((i, item) in element.withIndex()) {
                    if (i > 0) sb.append(", ")
                    pyDumps(item, sb)
                }
                sb.append(']')
            }
            is JsonPrimitive -> {
                if (element.isString) pyString(element.content, sb) else sb.append(element.content)
            }
        }
    }

    private fun pyString(s: String, sb: StringBuilder) {
        sb.append('"')
        for (ch in s) {
            when {
                ch == '"' -> sb.append("\\\"")
                ch == '\\' -> sb.append("\\\\")
                ch == '\n' -> sb.append("\\n")
                ch == '\r' -> sb.append("\\r")
                ch == '\t' -> sb.append("\\t")
                ch.code < 0x20 || ch.code > 0x7e ->
                    sb.append("\\u").append(ch.code.toString(16).padStart(4, '0'))
                else -> sb.append(ch)
            }
        }
        sb.append('"')
    }

    private fun pyDumps(element: JsonElement): String =
        StringBuilder().also { pyDumps(element, it) }.toString()

    /** Serialize a lineage Node the same way tools/gen_lineage_corpus.py does. */
    private fun serialize(node: Node, dialect: String): JsonObject {
        val children = node.downstream
            .map { serialize(it, dialect) }
            .sortedBy { pyDumps(it) }
        return buildJsonObject {
            put("name", node.name)
            put("expression", node.expression.sql(dialect))
            put("source", node.source.sql(dialect))
            put("source_name", node.sourceName)
            put("reference_node_name", node.referenceNodeName)
            put("downstream", buildJsonArray { for (c in children) add(c) })
        }
    }

    /** Re-sort the oracle's downstream arrays with the same canonical key. */
    private fun canonicalize(expected: JsonObject): JsonObject {
        val children = expected.getValue("downstream").jsonArray
            .map { canonicalize(it.jsonObject) }
            .sortedBy { pyDumps(it) }
        return buildJsonObject {
            for ((k, v) in expected) {
                if (k == "downstream") {
                    put(k, buildJsonArray { for (c in children) add(c) })
                } else {
                    put(k, v)
                }
            }
        }
    }

    @Test
    fun lineageCorpusMatchesPythonModuloLedger() {
        val root = json.parseToJsonElement(resource("lineage-corpus/base.json")).jsonObject
        val cases = root.getValue("cases").jsonArray.map { it.jsonObject }
        check(cases.isNotEmpty()) { "empty lineage corpus" }
        val ledger = loadLedger()

        val failures = LinkedHashMap<String, String>()
        var compared = 0

        for (case in cases) {
            val id = case.getValue("id").jsonPrimitive.content
            val sql = case.getValue("sql").jsonPrimitive.content
            val dialectName = case["dialect"]?.jsonPrimitive?.content ?: ""
            val trimSelects = case["trim_selects"]?.jsonPrimitive?.content != "false"
            val columnElem = case["column"] ?: JsonNull
            val schema = (case["schema"] as? JsonObject)?.let { toPlainMap(it) }
            val sources = (case["sources"] as? JsonObject)
                ?.mapValues { (_, v) -> v.jsonPrimitive.content as Any }
            val dialect = Dialects.forName(dialectName)

            compared++

            if (case["error"] != null) {
                // Python raised (SqlglotError etc.); we must raise too.
                try {
                    if (columnElem is JsonNull) {
                        lineageAll(sql, schema = schema, sources = sources, dialect = dialect,
                            trimSelects = trimSelects)
                    } else {
                        lineage(columnElem.jsonPrimitive.content, sql, schema = schema,
                            sources = sources, dialect = dialect, trimSelects = trimSelects)
                    }
                    failures[id] = "expected error ${case["error"]!!.jsonPrimitive.content}, none raised"
                } catch (e: Exception) {
                    // expected
                }
                continue
            }

            try {
                if (columnElem is JsonNull) {
                    val result = lineageAll(
                        sql, schema = schema, sources = sources, dialect = dialect,
                        trimSelects = trimSelects,
                    )
                    val expected = case.getValue("expected_columns").jsonObject
                    if (result.keys != expected.keys) {
                        failures[id] = "column set mismatch: expected ${expected.keys}, " +
                            "actual ${result.keys}"
                        continue
                    }
                    for ((name, node) in result) {
                        val exp = canonicalize(expected.getValue(name).jsonObject)
                        val act = serialize(node, dialectName)
                        if (pyDumps(exp) != pyDumps(act)) {
                            failures[id] = "graph mismatch for column '$name':\n" +
                                "      expected: ${pyDumps(exp)}\n      actual:   ${pyDumps(act)}"
                            break
                        }
                    }
                } else {
                    val node = lineage(
                        columnElem.jsonPrimitive.content, sql, schema = schema,
                        sources = sources, dialect = dialect, trimSelects = trimSelects,
                    )
                    val exp = canonicalize(case.getValue("expected").jsonObject)
                    val act = serialize(node, dialectName)
                    if (pyDumps(exp) != pyDumps(act)) {
                        failures[id] = "graph mismatch:\n      expected: ${pyDumps(exp)}\n" +
                            "      actual:   ${pyDumps(act)}"
                    }
                }
            } catch (e: Exception) {
                failures[id] = "${e::class.simpleName}: ${e.message}"
            }
        }

        val passed = compared - failures.size
        val rate = if (compared > 0) 100.0 * passed / compared else 100.0
        println(
            "LineageCorpusTest: $passed/$compared pass (${"%.1f".format(rate)}%), " +
                "${failures.size} failing"
        )

        // Always write the actual failure set in ledger format for easy regeneration.
        val actualLedger = buildJsonObject {
            put("cases", buildJsonArray {
                for ((key, reason) in failures) {
                    add(buildJsonObject { put("key", key); put("reason", reason) })
                }
            })
        }
        val outDir = java.io.File("build").takeIf { it.isDirectory } ?: java.io.File(".")
        java.io.File(outDir, "lineage-ledger-actual.json")
            .writeText(Json { prettyPrint = true }.encodeToString(JsonObject.serializer(), actualLedger))

        val problems = mutableListOf<String>()
        if (rate < 90.0) problems.add("below 90% gate: $passed/$compared")

        val unledgered = failures.keys - ledger.keys
        val stale = ledger.keys - failures.keys
        if (unledgered.isNotEmpty()) {
            problems.add(
                "${unledgered.size} UNLEDGERED failures (showing up to 20):\n" +
                    unledgered.take(20).joinToString("\n") { "  $it\n    reason: ${failures[it]}" }
            )
        }
        if (stale.isNotEmpty()) {
            problems.add(
                "${stale.size} STALE ledger entries now pass:\n" +
                    stale.take(20).joinToString("\n") { "  $it" }
            )
        }
        if (problems.isNotEmpty()) {
            fail(
                problems.joinToString("\n\n") +
                    "\n\nActual ledger written to ${java.io.File(outDir, "lineage-ledger-actual.json").absolutePath}"
            )
        }
    }
}
